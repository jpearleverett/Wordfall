export function getWordsByLength(min: number, max: number): string[] {
  const result: string[] = [];
  for (let len = min; len <= max; len++) {
    if (WORD_LISTS[len]) {
      result.push(...WORD_LISTS[len]);
    }
  }
  return result;
}

export function getAllWords(): Set<string> {
  const all = new Set<string>();
  for (const list of Object.values(WORD_LISTS)) {
    for (const w of list) all.add(w);
  }
  return all;
}
